import sys # ดึงเครื่องมือสำหรับสั่งปิดโปรแกรมมาใช้

# ========================================
# 🔒 ระบบล็อครหัสผ่านก่อนเข้าใช้งาน
# ========================================
print("="*40)
print("🔒 ระบบถูกล็อค กรุณายืนยันตัวตนก่อนใช้งาน")
print("="*40)

# 💡 คุณสามารถเปลี่ยนรหัสผ่านตรงนี้ได้เลยครับ (ตอนนี้ตั้งไว้เป็น student01)
secret_password = "student01" 

user_input = input("🔑 กรอกรหัสผ่านเพื่อเข้าสู่ระบบ: ")

if user_input != secret_password:
    print("\n❌ รหัสผ่านไม่ถูกต้อง! ไม่อนุญาตให้เข้าใช้งาน")
    sys.exit() # สั่งปิดโปรแกรมทันทีถ้ารหัสผิด

print("\n✅ รหัสผ่านถูกต้อง! กำลังเข้าสู่ระบบ...\n")

# ========================================
# 🔧 ระบบติดตามงานซ่อมบำรุง (Repair Tracking System)
# ========================================

# ฐานข้อมูลจำลองเก็บงานซ่อม (ใช้ Dictionary)
repair_jobs = {}
job_running_number = 1

# รายการสถานะงานซ่อมที่เป็นมาตรฐาน
status_list = [
    "รับเครื่อง / รอคิวตรวจเช็ค", 
    "กำลังตรวจเช็คอาการ", 
    "รออะไหล่", 
    "กำลังดำเนินการซ่อม", 
    "ซ่อมเสร็จแล้ว! (รอส่งมอบ)", 
    "ส่งมอบและชำระเงินเรียบร้อย (ปิดงาน)"
]

print("="*50)
print("🔧 ยินดีต้อนรับสู่ระบบจัดการงานซ่อม (Repair Master) 🔧")
print("="*50)

while True:
    print("\n--- 📋 เมนูหลัก ---")
    print("1. 📝 รับงานซ่อมใหม่ (สร้าง Job ID)")
    print("2. 🔄 อัปเดตสถานะงานซ่อม (สำหรับช่าง)")
    print("3. 🔍 ค้นหาสถานะงานซ่อม (สำหรับลูกค้า)")
    print("4. 📊 ดูงานซ่อมทั้งหมดในระบบ")
    print("0. 🚪 ออกจากระบบ")
    
    choice = input("👉 เลือกเมนู (0-4): ")
    
    # ----------------------------------------
    # เมนู 1: รับงานซ่อมใหม่
    # ----------------------------------------
    if choice == '1':
        print("\n[รับงานซ่อมใหม่]")
        customer_name = input("ชื่อลูกค้า: ")
        phone = input("เบอร์โทรศัพท์: ")
        item_name = input("อุปกรณ์ที่นำมาซ่อม (เช่น iPhone 13, Notebook ASUS): ")
        issue = input("อาการเสียเบื้องต้น: ")
        
        # สร้างรหัสงานซ่อม เช่น REP001
        job_id = f"REP{job_running_number:03d}"
        
        # เก็บข้อมูลลงฐานข้อมูล
        repair_jobs[job_id] = {
            "name": customer_name,
            "phone": phone,
            "item": item_name,
            "issue": issue,
            "status": 0, # เริ่มต้นที่สถานะ 0 (รับเครื่อง)
            "price": 0   # ประเมินราคาทีหลัง
        }
        job_running_number += 1
        
        print("-" * 40)
        print(f"✅ สร้างงานซ่อมสำเร็จ! รหัสงานซ่อมของคุณคือ: *** {job_id} ***")
        print("โปรดแจ้งรหัสนี้ให้ลูกค้า เพื่อใช้ติดตามสถานะครับ")
        print("-" * 40)

    # ----------------------------------------
    # เมนู 2: อัปเดตสถานะงาน (ช่างใช้งาน)
    # ----------------------------------------
    elif choice == '2':
        print("\n[อัปเดตสถานะงานซ่อม]")
        search_id = input("กรอกรหัสงานซ่อมที่ต้องการอัปเดต (เช่น REP001): ").upper()
        
        if search_id in repair_jobs:
            job = repair_jobs[search_id]
            print(f"\nรายการซ่อม: {job['item']} (ลูกค้า: {job['name']})")
            print(f"สถานะปัจจุบัน: {status_list[job['status']]}")
            
            print("\nเลือกสถานะใหม่:")
            for idx, status in enumerate(status_list):
                print(f"[{idx}] {status}")
                
            new_status = input("👉 เลือกหมายเลขสถานะใหม่: ")
            
            if new_status.isdigit() and int(new_status) < len(status_list):
                job['status'] = int(new_status)
                
                # ถ้าซ่อมเสร็จ (สถานะ 4) ให้ช่างใส่ราคาค่าซ่อมด้วย
                if int(new_status) == 4:
                    price = input("กรอกราคาค่าซ่อม (บาท): ")
                    job['price'] = int(price) if price.isdigit() else 0
                    
                print(f"✅ อัปเดตสถานะงาน {search_id} เป็น '{status_list[int(new_status)]}' เรียบร้อยแล้ว")
            else:
                print("❌ หมายเลขสถานะไม่ถูกต้อง")
        else:
            print(f"❌ ไม่พบรหัสงานซ่อม {search_id} ในระบบ")

    # ----------------------------------------
    # เมนู 3: ค้นหาสถานะ (ลูกค้าใช้งาน)
    # ----------------------------------------
    elif choice == '3':
        print("\n[ค้นหาสถานะงานซ่อม]")
        search_id = input("กรอกรหัสงานซ่อมของคุณ (เช่น REP001): ").upper()
        
        if search_id in repair_jobs:
            job = repair_jobs[search_id]
            print("\n" + "="*40)
            print("📱 รายละเอียดงานซ่อม")
            print("="*40)
            print(f"รหัสงาน:     {search_id}")
            print(f"อุปกรณ์:     {job['item']}")
            print(f"อาการเสีย:   {job['issue']}")
            print(f"เจ้าของเครื่อง: {job['name']}")
            print("-" * 40)
            print(f"📌 สถานะปัจจุบัน: >> {status_list[job['status']]} <<")
            
            if job['status'] >= 4:
                print(f"💰 ค่าใช้จ่ายทั้งหมด: {job['price']} บาท")
            print("="*40)
        else:
            print("❌ ไม่พบรหัสงานซ่อมในระบบ กรุณาตรวจสอบรหัสอีกครั้ง")

    # ----------------------------------------
    # เมนู 4: ดูงานทั้งหมด (Admin)
    # ----------------------------------------
    elif choice == '4':
        print("\n[รายการงานซ่อมทั้งหมดในระบบ]")
        if len(repair_jobs) == 0:
            print("--- ยังไม่มีงานซ่อมในระบบ ---")
        else:
            for j_id, j_data in repair_jobs.items():
                print(f"[{j_id}] {j_data['item']} | สถานะ: {status_list[j_data['status']]}")

    # ----------------------------------------
    # เมนู 0: ออกจากระบบ
    # ----------------------------------------
    elif choice == '0':
        print("\nขอบคุณที่ใช้ระบบ Repair Master ครับ...")
        break
        
    else:
        print("\n❌ เลือกเมนูไม่ถูกต้อง กรุณาพิมพ์ตัวเลข 0-4 เท่านั้น")